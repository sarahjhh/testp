$(document).ready(function(){
    $("#all").click(function(){
        $(".done").show();
        $(".in-progress").show();
        $(".wish").show();
        $(this).attr('class','red');
        $("#b_done").attr('class','blue');
        $("#b_in-progress").attr('class','blue');
        $("#b_wish").attr('class','blue');
    });
    $("#b_done").click(function(){
        $(".done").show();
        $(".in-progress").hide();
        $(".wish").hide();
        $(this).attr('class','red');
        $("#b_in-progress").attr('class','blue');
        $("#b_wish").attr('class','blue');
        $("#all").attr('class','blue');
    });
    $("#b_in-progress").click(function(){
        $(".done").hide();
        $(".in-progress").show();
        $(".wish").hide(); 
        $(this).attr('class','red');
        $("#b_done").attr('class','blue');
        $("#b_wish").attr('class','blue');
        $("#all").attr('class','blue');
    });
    $("#b_wish").click(function(){
        $(".done").hide();
        $(".in-progress").hide();
        $(".wish").show();
        $(this).attr('class','red');
        $("#b_in-progress").attr('class','blue');
        $("#b_done").attr('class','blue');
        $("#all").attr('class','blue');
    });  
});
